public class Item {
    // represent an item to be carried by the rockets
    private String name;
    private int weight;

    public int getWeight() {
        return this.weight;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
